// filter() মেথডটি মূল অ্যারেকে পরিবর্তন করে না, বরং নতুন অ্যারে তৈরি করে।
// এটি ব্যবহার করে আপনি অ্যারের নির্দিষ্ট শর্তসমূহকে পূরণকারী উপাদানগুলি বেছে নিতে পারেন।
//উদাহরণস্বরূপ,যদিআপনিএকটিডেটা-সেটথেকেকিছুনির্দিষ্টআইটেমফিল্টারকরতেচান,তাহলেfilter()মেথডটিখুবইকার্যকরহতেপারে। 

// এছাড়াও filter() মেথডটি একটি ফাংশন প্যারামিটার হিসেবে গ্রহণ করে যা প্রতিটি উপাদান, তার ইনডেক্স এবং পুরো অ্যারেকে আর্গুমেন্ট হিসেবে নেয়। এই ফাংশনটি একটি বুলিয়ান মান রিটার্ন করে যা নির্ধারণ করে যে উপাদানটি নতুন অ্যারেতে অন্তর্ভুক্ত হবে কিনা। উদাহরণস্বরূপ, আপনি ইনডেক্সের ভিত্তিতে ফিল্টার করতে পারেন বা অ্যারের দৈর্ঘ্যের ভিত্তিতে ফিল্টার করতে পারেন।
