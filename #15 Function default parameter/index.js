// কোডের মধ্যে ডিফল্ট প্যারামিটার ব্যবহার করা হয়েছে। যদি ফাংশন কল করার সময় কোন আর্গুমেন্ট না দেওয়া হয়, তবে ডিফল্ট মান 1 ব্যবহার করা হবে। যদি ডিফল্ট মান না দেওয়া হয় তাহলে ফাংশন কল করার সময় আর্গুমেন্ট দেওয়া না হলে undefined হবে। 

function myFunction(x = 1) {
    return x;
}

console.log(myFunction()); // Output: 1
console.log(myFunction(5)); // Output: 5


