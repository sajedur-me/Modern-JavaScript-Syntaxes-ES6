// কোডের ব্যাখ্যা: এই কোডটি একটি `Person` ক্লাস তৈরি করে যার মধ্যে একটি কনস্ট্রাক্টর এবং দুটি মেথড রয়েছে। `exampleFunction` মেথডে একটি অ্যারে তৈরি করা হয় এবং `find` মেথড ব্যবহার করে কলব্যাক ফাংশনগুলিতে `this.test()` কল করা হয়। প্রথম কলব্যাক ফাংশনটি একটি সাধারণ ফাংশন হিসেবে লেখা হয়েছে এবং দ্বিতীয়টি একটি অ্যারো ফাংশন হিসেবে লেখা হয়েছে। অ্যারো ফাংশনগুলি লেক্সিক্যাল `this` ব্যবহার করে, তাই এটি বাইরের কনটেক্সট থেকে `this` এর মান নেয় এবং সঠিকভাবে `test` মেথডটি কল করে। যখন `exampleFunction` কল করা হয়, তখন এটি কনসোলে "This is a test method." প্রিন্ট করবে।
class Person {
    constructor(name, age) {
        this.name = name;
        this.age = age;
    }

    test() {
        console.log("This is a test method.");
    }

    exampleFunction() {
        let array = [1, 2, 3, 4];
        // কোডের সংক্ষেপ ব্যাখ্যা: এখানে `find` মেথডের কলব্যাক ফাংশনটি একটি সাধারণ ফাংশন হিসেবে লেখা হয়েছে এবং `this` কিওয়ার্ডটি ব্যবহার করা হয়েছে। যেহেতু `this` এর মান কলব্যাক ফাংশনের মধ্যে পরিবর্তিত হতে পারে, তাই দ্বিতীয় প্যারামিটার হিসেবে `this` পাস করা হয়েছে যাতে কলব্যাক ফাংশনের ভিতরে `this.test()` সঠিকভাবে কাজ করে।
        array.find(function() {
            this.test();
        }, this);

        // কোডের সংক্ষেপ ব্যাখ্যা: এখানে `find` মেথডের কলব্যাক ফাংশনটি একটি অ্যারো ফাংশন হিসেবে লেখা হয়েছে। অ্যারো ফাংশনগুলি লেক্সিক্যাল `this` ব্যবহার করে, যার মান বাইরের কনটেক্সট থেকে নেওয়া হয়। তাই এখানে `this.test()` সঠিকভাবে কাজ করবে এবং `test` মেথডটি কল করা হবে।
        array.find(() => {
            this.test();
        });
    }
}

let person = new Person("Sajedur", 27);
person.exampleFunction();


