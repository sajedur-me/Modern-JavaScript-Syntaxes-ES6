// কোডের ব্যাখ্যা: এই কোডটি একটি অ্যারে `numbers` তৈরি করে যার মধ্যে ১ থেকে ১০ পর্যন্ত সংখ্যাগুলি রয়েছে। তারপর `filter` মেথড ব্যবহার করে একটি ফাংশন পাস করা হয় যা প্রতিটি সংখ্যাকে পরীক্ষা করে। `return` স্টেটমেন্টটি চেক করে যে সংখ্যা ২দিবসিক কিনা। যদি সংখ্যা ২দিবসিক হয়, তাহলে সেটি `evenNumbers` অ্যারেতে যোগ করা হবে। অবশেষে, `evenNumbers` অ্যারেটি কনসোল-এ প্রিন্ট করা হয়, যা শুধুমাত্র ২, ৪, ৬, ৮ এবং ১০ থাকবে।

