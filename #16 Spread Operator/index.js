// Spread Operator in JavaScript
// কোডের মধ্যে স্প্রেড অপারেটর ব্যবহার করে একটি অ্যারে থেকে মান কপি করে নতুন অ্যারে তৈরি করা হয়েছে। এখানে আমরা দেখব কিভাবে স্প্রেড অপারেটর কাজ করে এবং এর মাধ্যমে আমরা একটি অ্যারে থেকে মান কপি করে নতুন অ্যারে তৈরি করতে পারি। যেমন উদাহরণে, আমরা একটি numbers অ্যারে তৈরি করেছি এবং তারপর a এবং b নামে দুটি নতুন অ্যারে তৈরি করেছি। a তে আমরা numbers অ্যারের রেফারেন্স শেয়ার করেছি, তাই a তে পরিবর্তন করলে numbers তেও পরিবর্তন হবে। কিন্তু b তে আমরা স্প্রেড অপারেটর ব্যবহার করে একটি নতুন অ্যারে তৈরি করেছি যা numbers থেকে মান কপি করে তৈরি করা হয়েছে, তাই b তে কোন পরিবর্তন হয়নি যখন a তে পরিবর্তন করা হয়েছে।

const numbers = [1, 2, 3];

const a = numbers;
const b = [...numbers];

console.log(a); // Output: [1, 2, 3]
console.log(b); // Output: [1, 2, 3]
console.log(numbers); // Output: [1, 2, 3]

a.push(4);
console.log(a); // Output: [1, 2, 3, 4]
console.log(b); // Output: [1, 2, 3] - b তে কোন পরিবর্তন হয়নি কারণ এটি একটি নতুন অ্যারে যা numbers থেকে মান কপি করে তৈরি করা হয়েছে, তাই b তে কোন পরিবর্তন হয়নি।

console.log(numbers); // Output: [1, 2, 3, 4] - a তে পরিবর্তন করার ফলে numbers অ্যারে তেও পরিবর্তন হয়েছে কারণ a এবং numbers একই রেফারেন্স শেয়ার করে। কিন্তু b একটি নতুন অ্যারে তৈরি করে এবং numbers থেকে মান কপি করে, তাই b তে কোন পরিবর্তন হয়নি।


// স্প্রেড অপারেটর ব্যবহার করে একটি অবজেক্ট থেকে মান কপি করে নতুন অবজেক্ট তৈরি করা হয়েছে। এখানে আমরা দেখব কিভাবে স্প্রেড অপারেটর কাজ করে এবং এর মাধ্যমে আমরা একটি অবজেক্ট থেকে মান কপি করে নতুন অবজেক্ট তৈরি করতে পারি। যেমন উদাহরণে, আমরা একটি obj1 অবজেক্ট তৈরি করেছি এবং তারপর obj2 নামে একটি নতুন অবজেক্ট তৈরি করেছি যা obj1 থেকে মান কপি করে তৈরি করা হয়েছে, তাই obj2 তে কোন পরিবর্তন হয়নি যখন obj1 তে পরিবর্তন করা হয়েছে। 
const obj1 = { name: 'Alice', age: 30 };
const obj2 = { ...obj1, city: 'New York' };

console.log(obj1); // Output: { name: 'Alice', age: 30 }
console.log(obj2); // Output: { name: 'Alice', age: 30, city: 'New York' }
